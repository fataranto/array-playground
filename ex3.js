// https://developer.mozilla.org/es/docs/Web/JavaScript/Reference/Global_Objects/Array/fill

// usa el método 'fill' adecuadamente para substituir todos los números por '0' en array 'numeros'

let numeros = [1, 2, 3, 4, 5];

// El método fill NO crea un array nuevo; tan solo modifica los elementos del array.
console.log(numeros.fill(0)); 
